         public boolean validatetable_update(){
     
  
     String  VenueName = txtVenueName.getText();
         String  Place = txtCapacity.getText(); 
         String  Contact = txtContact.getText();
         
         if(VenueName.equals("")){
             
             JOptionPane.showMessageDialog(this, "please enter the venue name");
             return false;
         }
         
           if(Place.equals("")){
             
             JOptionPane.showMessageDialog(this, "please enter the venue's Capacity");
             return false;
         }
           
             if(Contact.equals("")){
             
             JOptionPane.showMessageDialog(this, "please enter the venue's Contact number");
             return false;
         }
         return true;
      } 
  
