         public boolean validatebooking(){
     
    
         String  Location =  txtLocation.getText();
         String  Date =  txtDate.getText();
         String NoOfGuest = txtNoOfGuest.getText();
         String  Equipments =  txtEquipments.getText();
         String  Cost = txtCost.getText();
   
             if( Location.equals("")){
             
             JOptionPane.showMessageDialog(this, "please enter conference location");
             return false;
         }
           if(Date.equals("")){
             
             JOptionPane.showMessageDialog(this, "please enter conference Date ");
             return false;
         }
   
           
             if( NoOfGuest.equals("")){
             
             JOptionPane.showMessageDialog(this, "please enter the number of Guest");
             return false;
         }
          
             if( Equipments.equals("")){
             
             JOptionPane.showMessageDialog(this, "What Equipments are to be used");
             return false;
         }
         
             if( Cost.equals("")){
             
             JOptionPane.showMessageDialog(this, " please enter the total Cost if need be!");
             return false;
         }
         return true;
      } 
        
            
            
            
            
