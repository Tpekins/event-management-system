     private void table_update() { 
          int c;
          
       try {
          
            String query ="( SELECT * FROM `book event/conference`)";
           con = DriverManager.getConnection("jdbc:mysql://localhost:3308/userlogin", "root", "");
           insert =con.prepareStatement(query);
             ResultSet rs = insert.executeQuery();
             ResultSetMetaData Rss = rs.getMetaData();
             c = Rss.getColumnCount();
             
             
             DefaultTableModel Df = (DefaultTableModel)jTable1.getModel();
             Df.setRowCount(0);
             
                 
             
             while(rs.next())
             {
             
             Vector v2 = new Vector();
      
                 for(int a=1; a<=c; a++)
               {
                   
                 v2.add(rs.getString("id"));
                 v2.add(rs.getString("Location"));
                 v2.add(rs.getString("Date"));
                 v2.add(rs.getString("No Of Guest"));
                     v2.add(rs.getString("Equipments"));
                 v2.add(rs.getString("Cost"));
               }
             
             Df.addRow(v2);
       }  
                 
       }  
   
        catch (Exception ex) {
            Logger.getLogger(managevenue.class.getName()).log(Level.SEVERE, null, ex);
    }     
 }                                      
 
           
     
     
     
